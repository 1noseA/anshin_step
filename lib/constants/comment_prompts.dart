/// コメント生成用のプロンプトを定義するクラス
class CommentPrompts {
  /// ベイビーステップの実施結果に対するコメントを生成するプロンプトを作成
  static String generateCommentPrompt({
    required String profileContext,
    required String action,
    required String beforeAnxietyScore,
    required String afterAnxietyScore,
    required String userComment,
  }) {
    return '''
$profileContext

以下のベイビーステップの実施結果について、感情分析を行い、ユーザーに寄り添ったあたたかいコメントを作成してください。

【不安得点について】
- 不安得点は0-100の範囲で、数値が低いほど不安が少ないことを示します
- 例：10点は不安が少ない状態、90点は強い不安を感じている状態です

【実施結果】
・どのベイビーステップか: $action
・事前不安得点: $beforeAnxietyScore
・事後不安得点: $afterAnxietyScore
・ユーザーコメント: $userComment

【コメント作成時の注意】
- 「失敗」という概念はない。行動を起こしたことがまずすごいし、うまくいかないと思っても「改善点を見つけた」という捉え方を促す。
- 「安心感」「ユーザー自分自身との信頼」が育つようなコメントにする。
- 感情分析をした結果を盛り込んで、ユーザーの気持ちに寄り添ったコメントをする。
- プロフィール情報の名前を使用して「○○さん」と呼びかけてください。
- コメントは以下の形式で出力してください：
  ## 感情分析結果
  [感情分析の内容]

  [寄り添ったあたたかいコメント]
- 「**」は使用しないこと。
''';
  }

  /// 専門家受診レコメンド用プロンプト
  static String generateRecommendPrompt({
    required String profileContext,
    required String action,
    required String beforeAnxietyScore,
    required String afterAnxietyScore,
    required String userComment,
  }) {
    return '''
$profileContext

以下のベイビーステップの実施結果とコメント内容をもとに、このユーザーが精神科やカウンセリングなど専門家のサポートを受けることを推奨すべきか判定してください。

【判定ルール】
以下のいずれかに該当する場合は「推奨」とし、簡潔な理由を述べてください。それ以外は「不要」とし、理由を述べてください。
※ 診断名がついているだけで、コメントに深刻な内容がなければ「不要」と判定してください。
※ 日常的な不安や軽度の症状のみの場合は「不要」としてください。

【推奨条件】
1. ユーザーが「死にたい」「消えたい」など希死念慮を明言している場合
2. 他者への攻撃性や暴力的な内容が含まれる場合
3. 動悸・息切れ・意識の喪失など体調急変が示されている場合
4. 明確な生活機能の低下（例：食事が取れない、外に出られない、眠れない状態が続いている）を示している場合
5. 不安得点が著しく悪化している（after > before かつ afterが高スコア）場合
6. 過去に精神疾患歴があり再発が疑われる言及がある場合

【出力フォーマット】
- 判定: 「推奨」または「不要」
- 理由: 簡潔に明記してください

【実施結果】
・どのベイビーステップか: $action
・事前不安得点: $beforeAnxietyScore
・事後不安得点: $afterAnxietyScore
・ユーザーコメント: $userComment

【例】
1. ユーザーコメント:「死んでしまいたい」→ 判定: 推奨 理由: 希死念慮が明確に示されているため。
2. ユーザーコメント:「誰かを殴ってやりたい気分」→ 判定: 推奨 理由: 他者への攻撃性があるため。
3. ユーザーコメント:「動悸と吐き気で立っていられない」→ 判定: 推奨 理由: 体調の急変がみられるため。
4. ユーザーコメント:「何も食べられず3日寝ていない」→ 判定: 推奨 理由: 生活機能の著しい低下がみられるため。
5. ユーザーコメント:「やってみたけど不安が増した」 不安スコア: 50→90 → 判定: 推奨 理由: 不安の悪化がみられるため。
6. ユーザーコメント:「以前うつ病だったが、また何もできなくなってきた」→ 判定: 推奨 理由: 再発が疑われるため。
7. ユーザーコメント:「不安だったがやってみたら楽になった」→ 判定: 不要 理由: ベイビーステップで改善が見られたため。
8. ユーザーコメント:「診断名があるが、特に困っていない」→ 判定: 不要 理由: コメントに深刻な内容がないため。
''';
  }
}
