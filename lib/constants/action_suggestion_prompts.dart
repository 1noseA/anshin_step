import 'dart:convert';

class ActionSuggestionPrompts {
  /// 入力内容を要約・成形するためのプロンプト（登録・表示用）
  static String generateContentSummaryPrompt({
    required String content,
    String role = '認知行動療法の専門家であり、公認心理師・臨床心理士二つの有資格者です',
    String profileContext = '',
  }) {
    return '''
あなたは$role。
ユーザーの入力内容を分析し、以下の点に注意して要約・成形してください。

【入力内容の分析】
1. 主要な目標や願望の抽出
   - 達成したいこと
   - 改善したいこと
   - 克服したいこと
   - やりたいこと
   - やらなければならないこと

2. 不安や懸念の抽出
   - 不安なこと
   - 心配なこと
   - 障害となること
   - 悩み
   - 困っていること

3. コンテキストの理解
   - 状況
   - 背景
   - 制約条件

【要約・成形の注意点】
- 入力内容を30文字程度に要約
- 目標と不安の両方を含める
- 具体的で明確な表現を使用
- 前向きな表現を心がける

$profileContext

入力内容: $content

以下の形式で要約してください：
[要約された内容]
''';
  }

  /// 入力内容を要約・成形するためのプロンプト（ステップ生成用）
  static String generateContentForStepsPrompt({
    required String content,
    String role = '認知行動療法の専門家であり、公認心理師・臨床心理士二つの有資格者です',
    String profileContext = '',
  }) {
    return '''
あなたは$role。
ユーザーの入力内容を分析し、以下の点に注意して要約・成形してください。

【入力内容の分析】
1. 主要な目標や願望の抽出
   - 達成したいこと
   - 改善したいこと
   - 克服したいこと
   - やりたいこと
   - やらなければならないこと

2. 不安や懸念の抽出
   - 不安なこと
   - 心配なこと
   - 障害となること
   - 悩み
   - 困っていること

3. コンテキストの理解
   - 状況
   - 背景
   - 制約条件

【要約・成形の注意点】
- 入力内容を2行程度（100文字以内）に要約
- 1行目：目標や願望を簡潔に表現
- 2行目：不安や懸念を簡潔に表現
- 具体的で明確な表現を使用
- 前向きな表現を心がける
- 改行は「\\n」を使用

$profileContext

入力内容: $content

以下の形式で要約してください：
[目標や願望の要約]\\n[不安や懸念の要約]
''';
  }

  /// ベイビーステップ生成用のプロンプト
  static String generateBabyStepsPrompt({
    required String content,
    String role = '認知行動療法の専門家であり、公認心理師・臨床心理士二つの有資格者です',
    String profileContext = '',
  }) {
    return '''
あなたは$role。
ユーザーの入力内容を分析し、以下の点に注意して10個の具体的なベイビーステップを提案してください。

【入力内容の分析】
1. 目標要素の抽出
   - 達成したいこと
   - 改善したいこと
   - 克服したいこと

2. 不安要素の抽出
   - 心配なこと
   - 障害となること
   - 悩み

3. コンテキストの理解
   - 状況
   - 背景
   - 制約条件

【ステップ生成の注意点】
- 入力内容が不明確な場合は、最も安全で基本的なステップから始める
- 不安要素が明確でない場合は、目標達成のための最小限のステップを提案
- 目標が不明確な場合は、不安の軽減に焦点を当てたステップを提案

$profileContext

入力内容: $content

以下の形式で10個のベイビーステップを提案してください：
1. [具体的なステップ]
2. [具体的なステップ]
...

各ステップは以下の点に注意して作成してください：
- 各ステップは1行で簡潔に表現
- 段階的に難易度が上がるように構成
- 認知行動療法の不安階層表や暴露療法の知識を活用
- 不安な人が使っているアプリなので無理のない行動を提案する
''';
  }
}
